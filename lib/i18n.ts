export function translate(language: AppLanguage, key: TranslationKey, params?: Record<string, string | number>) {
  const template = translations[language]?.[key] ?? translations.de[key] ?? key;
  if (!params) return template;
  return Object.entries(params).reduce(
    (value, [paramKey, paramValue]) => value.replaceAll(`{${paramKey}}`, String(paramValue)),
    template,
  );
}

export function getWeekdayLabel(language: AppLanguage, value: WeekStartsOn) {
  const option = weekStartsOnOptions.find((item) => item.value === value);
  return option?.[language] ?? option?.de ?? value;
}
